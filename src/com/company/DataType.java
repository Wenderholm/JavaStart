package com.company;

public class DataType {
    public static void main(String[] args) {
        System.out.print("Liczba int: ");
        System.out.print(25);
        System.out.print('\n');

        System.out.print("Liczba double: ");
        System.out.print(25.75);
        System.out.print('\n');

        System.out.print("Znak A: ");
        System.out.print('A');
        System.out.print('\n');

        System.out.print("Czy uczę się programowania? ");
        System.out.println(true);

        System.out.println("-Cześć Jan, słyszałem, że masz " + 25 + " lat i uczysz się programowania. Czy to prawda?");
        System.out.println("-" + true);
        System.out.println("-Czy Twoje inicjały to J K?");
        System.out.println("-Tak," + true);
    }
}
